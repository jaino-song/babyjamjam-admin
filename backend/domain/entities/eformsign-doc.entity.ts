export const EFORMSIGN_DOCUMENT_KIND = {
    CONTRACT: "contract",
    SERVICE_RECORD_SNAPSHOT: "service_record_snapshot",
} as const;

export type EformsignDocumentKind =
    (typeof EFORMSIGN_DOCUMENT_KIND)[keyof typeof EFORMSIGN_DOCUMENT_KIND];

const EFORMSIGN_DOCUMENT_KIND_VALUES = new Set<string>(Object.values(EFORMSIGN_DOCUMENT_KIND));

interface EformsignDocProps {
    id?: number; // auto-generated by DB, optional on create
    documentId: string;
    createdDate: Date;
    updatedDate: Date;
    // doc status code example: 060 = pending, 050 = completed, 080 = rejected
    statusType: string;
    // doc status detail example: "대기", "완료", "거부"
    statusDetail: string;
    // step type example: "05" = participant, "06" = reviewer
    stepType: string;
    // step index example: "2"
    stepIndex: string;
    // step name example: "이용자 성명"
    stepName: string;
    // step recipients example: [{ type: "05", name: "이용자 성명", sms: "010-1234-5678" }]
    stepRecipientType: string;
    // step recipient name example: ["이용자 성명"]
    stepRecipientName: string;
    // step recipient sms example: ["010-1234-5678"]
    stepRecipientSms: string;
    // expired date example: 2025-12-13
    expiredDate: Date;
    // expired example: true
    expired: boolean;
    // FK to client
    clientId: number;
    // document classification. null means an older/unclassified row.
    documentKind?: EformsignDocumentKind | null;
    // FK to employee_schedule for service feedback snapshots.
    employeeScheduleId?: number | null;
    // Source eformsign template identifier.
    templateId?: string | null;
}

